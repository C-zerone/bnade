package com.bnade.wow.vo;

public class HotItemVo {
	private int id;
	private String name;
	private int queried;
	private int type;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getQueried() {
		return queried;
	}

	public void setQueried(int queried) {
		this.queried = queried;
	}

	public int getType() {
		return type;
	}

	public void setType(int type) {
		this.type = type;
	}

}
